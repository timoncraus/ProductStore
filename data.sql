SELECT p.id, 
       CASE 
           WHEN p.unit_id = 1 THEN 4  -- 1 кг для кг
           WHEN p.unit_id = 2 THEN 7  -- 200 г для грамм
           WHEN p.unit_id = 3 THEN 3  -- большая для литров
           ELSE 9  -- 1 шт для штук
       END as package_id,
       FLOOR(RAND() * 1000) as stock_quantity,
       CONCAT('SKU-', p.id, '-', FLOOR(RAND() * 10000))
FROM product p
WHERE NOT EXISTS (SELECT 1 FROM product_package pp WHERE pp.product_id = p.id);

